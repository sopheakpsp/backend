<h2>Law &amp; Regulation</h2>
<p><b>A Law</b> is adopted by the National Assembly and the Senate, and promulgated by the King or the acting Head of State. A law must be in strict conformity with the Constitution.</p>

<p><b>A Royal Decree</b> is issued by the King in the exercise of his constitutional powers. A Royal decree must be in strict conformity with the Constitution.</p>

<p><b>A Sub-Decree</b> is adopted by the Council of Ministers and signed by the Prime Minister. A sub-decree must be in strict conformity with the Constitution and conform to the Law to which it refers.</p>

<p><b>A Prokas</b> is a ministerial or inter-ministerial decision signed by the relevant Minister(s). A proclamation must conform to the Constitution and to the law or sub-decree to which it refers.</p>

<p><b>A Circular</b> is an instrument that a Ministry or higher authority use to clarify a point of law or to provide instructions. A circular is only advisory and does not have the force of law.</p>

